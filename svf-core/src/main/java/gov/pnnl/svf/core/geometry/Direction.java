package gov.pnnl.svf.core.geometry;

/**
 * Represents a general direction.
 * <p>
 * directions {back, front, right, left, top, bottom}
 */
public enum Direction {
    /**
     * Back (South)
     */
    BACK,
    /**
     * Front (North)
     */
    FRONT,
    /**
     * Right (East)
     */
    RIGHT,
    /**
     * Left (West)
     */
    LEFT,
    /**
     * Top (Up)
     */
    TOP,
    /**
     * Bottom (Down)
     */
    BOTTOM

}
