package gov.pnnl.svf.core.constant;

/**
 * Constant interface related to Cartesian coordinates.
 *
 * @author Arthur Bleeker
 */
public interface DimensionConst {

    /**
     * Number representing a one dimension space (1D).
     */
    int ONE_D = 1;
    /**
     * Number representing a two dimension space (2D).
     */
    int TWO_D = 2;
    /**
     * Number representing a three dimension space (3D).
     */
    int THREE_D = 3;
    /**
     * Number representing a four dimension space (4D).
     */
    int FOUR_D = 3;

}
