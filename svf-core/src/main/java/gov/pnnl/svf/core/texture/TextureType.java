package gov.pnnl.svf.core.texture;

/**
 * Type of texture.
 *
 * @author Arthur Bleeker
 */
public enum TextureType {

    /**
     * Alpha texture type
     */
    ALPHA,
    /**
     * Color RGB texture with no alpha channel
     */
    RGB,
    /**
     * Color RGBA texture with an alpha channel
     */
    RGBA
}
